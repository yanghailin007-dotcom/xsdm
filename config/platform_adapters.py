# 平台适配器工厂
class PlatformAdapterFactory:
    """平台适配器工厂"""
    
    _adapters = {
        "fanqie": FanqieAdapter,
        "qidian": QidianAdapter,
        "zhihu": ZhihuAdapter
    }
    
    @classmethod
    def get_adapter(cls, platform_name: str) -> PlatformAdapter:
        """获取指定平台的适配器"""
        adapter_class = cls._adapters.get(platform_name.lower())
        if not adapter_class:
            # 默认使用番茄适配器
            return FanqieAdapter()
        return adapter_class()
    
    @classmethod
    def get_supported_platforms(cls) -> list:
        """获取支持的平台列表"""
        return [
            {"code": "fanqie", "name": "番茄小说", "description": "免费爽文平台，快节奏、高爽点"},
            {"code": "qidian", "name": "起点中文网", "description": "付费阅读平台，重世界观、深度剧情"},
            {"code": "zhihu", "name": "知乎盐选", "description": "精品短篇平台，脑洞、反转、共鸣"}
        ]


