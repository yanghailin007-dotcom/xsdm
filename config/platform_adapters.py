"""
网站平台适配配置
定义不同小说平台的读者偏好和生成风格
"""

class PlatformAdapter:
    """平台适配器基类"""
    
    def __init__(self, platform_name: str):
        self.platform_name = platform_name
    
    def get_prompt_context(self) -> str:
        """获取平台适配的提示词上下文"""
        raise NotImplementedError
    
    def get_title_style_guide(self) -> str:
        """获取标题风格指导"""
        raise NotImplementedError
    
    def get_content_style_guide(self) -> str:
        """获取内容风格指导"""
        raise NotImplementedError
    
    def get_preferred_genres(self) -> list:
        """获取偏好的类型标签"""
        raise NotImplementedError
    
    def get_core_keywords(self) -> list:
        """获取核心关键词"""
        raise NotImplementedError


class FanqieAdapter(PlatformAdapter):
    """番茄小说平台适配器"""
    
    def __init__(self):
        super().__init__("番茄小说")
    
    def get_prompt_context(self) -> str:
        return """
**目标平台：番茄小说**
番茄小说是字节跳动旗下的免费阅读平台，用户群体庞大，偏好快节奏、强代入感的爽文。

**平台特点：**
1. **读者画像**：以年轻读者为主（18-35岁），喜欢轻松、直接的阅读体验
2. **阅读场景**：碎片化阅读为主，需要快速吸引注意力
3. **付费模式**：免费阅读+广告变现，依靠高留存率和完读率
4. **推荐机制**：基于完读率、追读率、互动数据推荐

**内容偏好：**
- **黄金三章**：前3章必须有强烈冲突、金手指激活、打脸逆袭
- **高爽点密度**：每章都要有小高潮，每3-5章一个大高潮
- **强代入感**：主角设定贴近普通人，让读者容易代入
- **直白易懂**：语言简洁，避免复杂设定和长篇解释
- **情绪调动**：愤怒→压抑→爆发→爽快的情绪曲线

**热门类型：**
都市修真、系统流、神豪、重生、穿越、赘婿、战神、脑洞文、无敌流、打脸爽文

**避免元素：**
- 复杂世界观设定（特别是需要大量解释的）
- 慢热剧情
- 过于文艺的表达
- 情感虐文
- 悬疑推理
"""
    
    def get_title_style_guide(self) -> str:
        return """
**番茄小说标题风格指南：**

1. **字数要求**：6-14字，包含标点
2. **命名公式**：
   - 金手指 + 身份（如：神豪+医生 = 《都市神豪医生》）
   - 核心爽点 + 时间点（如：重生+2010 =《重生2010之神豪崛起》）
   - 系统功能 + 搞笑描述（如：签到系统 =《开局签到：获得神级跑车》）
   - 反差设定（如：保安+亿万身家 =《保安竟是亿万富翁》）

3. **高点击关键词**：
   【神豪、系统、重生、穿越、无敌、签到、开局、都市、修真、赘婿、战神、奶爸、神医、学霸、总裁、女帝、反派】

4. **避免风格**：
   - 文艺化标题（如《星辰大海的约定》）
   - 悬念式标题（如《那年的秘密》）
   - 过长标题（超过14字）
   - 生僻词汇
"""
    
    def get_content_style_guide(self) -> str:
        return """
**番茄小说内容风格指南：**

1. **开篇要求（黄金三章）**：
   - 第1章：必须出现强烈冲突（被羞辱、被背叛、陷入绝境）
   - 第2章：金手指激活/系统觉醒，获得翻盘机会
   - 第3章：首次打脸/展示实力，确立爽文基调

2. **节奏控制**：
   - 每2000字一个小转折
   - 每6000字一个小高潮
   - 每2-3章一个大爽点
   - 严禁超过5000字的平淡描写

3. **角色设定**：
   - 主角：普通人或落魄者（让读者代入）
   - 性格：果断、腹黑、有底线但不受欺负
   - 成长：快速升级，能力提升要直观可见

4. **对话风格**：
   - 简短有力，2-3句话为一段
   - 多用感叹号、反问
   - 包含情绪化表达（愤怒、嘲讽、震惊）

5. **场景设计**：
   - 优先选择都市场景（豪宅、公司、夜店、医院）
   - 每个场景都有明确的冲突点
   - 环境描写简洁（不超过50字）
"""
    
    def get_preferred_genres(self) -> list:
        return [
            "都市修真", "都市高武", "都市日常", "都市脑洞",
            "系统流", "神豪", "重生", "穿越", "赘婿", "战神",
            "脑洞文", "无敌流", "打脸爽文", "奶爸", "神医",
            "学霸", "扮猪吃虎", "黑科技", "签到"
        ]
    
    def get_core_keywords(self) -> list:
        return [
            "神豪", "系统", "重生", "穿越", "无敌", "签到", "开局",
            "都市", "修真", "赘婿", "战神", "奶爸", "神医", "学霸",
            "总裁", "女帝", "反派", "打脸", "逆袭", "暴富", "神级",
            "万倍", "十倍", "百倍", "千亿", "万亿"
        ]


class QidianAdapter(PlatformAdapter):
    """起点中文网平台适配器"""
    
